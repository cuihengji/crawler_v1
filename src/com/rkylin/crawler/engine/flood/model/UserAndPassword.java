package com.rkylin.crawler.engine.flood.model;

public class UserAndPassword {
    private int appAccountIndex;
    private String user;
    private String pass;

    public int getAppAccountIndex() {
        return appAccountIndex;
    }

    public void setAppAccountIndex(int appAccountIndex) {
        this.appAccountIndex = appAccountIndex;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

}
