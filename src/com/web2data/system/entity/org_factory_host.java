package com.web2data.system.entity;

public class org_factory_host {

	
	private int id = -1;
	private String status = null;
	private String name = null;
	private int senior_workers = -1;
	private int junior_workers = -1;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSenior_workers() {
		return senior_workers;
	}
	public void setSenior_workers(int senior_workers) {
		this.senior_workers = senior_workers;
	}
	public int getJunior_workers() {
		return junior_workers;
	}
	public void setJunior_workers(int junior_workers) {
		this.junior_workers = junior_workers;
	}
	
}
